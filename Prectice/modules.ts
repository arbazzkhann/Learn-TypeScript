//────────────────────────────────── Modules (import / export)
/*
Modules in TypeScript enable code organization, encapsulation, and reusability 
by "splitting code into separate", self-contained files that execute in their own scope.

Using ES6 "export" and "import" syntax, variables, functions, classes, and interfaces 
can be shared between files. 

Any file containing a top-level import or export is considered a module. 


Named Exports: export const myVar = 1;
Default Exports: export default MyClass;


import { MyClass } from "./MyClass";
import MyDefault from "./MyDefault";
*/